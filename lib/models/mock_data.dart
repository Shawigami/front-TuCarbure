import 'package:flutter/foundation.dart';

class Station {
  String name;
  String address;
  double longitude;
  double latitude;
  List<Fuel> fuels;
  DateTime lastUpdate;

  Station({
    required this.name,
    required this.address,
    required this.longitude,
    required this.latitude,
    required this.fuels,
    required this.lastUpdate,
  });
}

class Fuel {
  String name;
  double price;
  DateTime lastUpdate;

  Fuel({
    required this.name,
    required this.price,
    required this.lastUpdate,
  });
}

